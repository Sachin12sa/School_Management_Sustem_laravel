    // ════════════════════════════════════════════════════════════════
    //  CORE: AD → BS
    // ════════════════════════════════════════════════════════════════

    public static function adToBs($date): array
    {
        if (!$date || $date === '0000-00-00' || $date === '0000-00-00 00:00:00') {
            return ['year' => 0, 'month' => 0, 'day' => 0, 'month_name' => ''];
        }

        $clean = substr((string) $date, 0, 10);

        if (!preg_match('/^\d{4}-\d{2}-\d{2}$/', $clean)) {
            return ['year' => 0, 'month' => 0, 'day' => 0, 'month_name' => ''];
        }

        $ad     = Carbon::parse($clean);
        $anchor = Carbon::parse(self::AD_ANCHOR);

        // Signed diff (Carbon::diffInDays is always unsigned, use timestamps)
        $diffDays = (int) round(($ad->timestamp - $anchor->timestamp) / 86400);

        if ($diffDays < 0 || $diffDays > 36524) {
            return ['year' => 0, 'month' => 0, 'day' => 0, 'month_name' => ''];
        }

        $bsYear  = self::BS_ANCHOR;
        $bsMonth = 1;
        $bsDay   = 1;
        $idx     = 0;
        $maxIdx  = count(self::$data) - 1;

        while ($diffDays > 0) {
            if ($idx > $maxIdx) {
                return ['year' => 0, 'month' => 0, 'day' => 0, 'month_name' => ''];
            }

            $daysInMonth = self::$data[$idx][$bsMonth];

            if ($diffDays < $daysInMonth) {
                $bsDay    = $diffDays + 1;
                $diffDays = 0;
            } else {
                $diffDays -= $daysInMonth;
                $bsMonth++;
                if ($bsMonth > 12) {
                    $bsMonth = 1;
                    $bsYear++;
                    $idx++;
                }
            }
        }

        return [
            'year'       => $bsYear,
            'month'      => $bsMonth,
            'day'        => $bsDay,
            'month_name' => self::$months[$bsMonth] ?? '',
        ];
    }

    // ════════════════════════════════════════════════════════════════
    //  CORE: BS → AD
    // ════════════════════════════════════════════════════════════════

    public static function bsToAd(int $y, int $m, int $d): Carbon
    {
        $totalDays = 0;
        $idx       = $y - self::BS_ANCHOR;

        for ($i = 0; $i < $idx; $i++) {
            $totalDays += self::$data[$i][0];
        }
        for ($mi = 1; $mi < $m; $mi++) {
            $totalDays += self::$data[$idx][$mi];
        }
        $totalDays += ($d - 1);

        return Carbon::parse(self::AD_ANCHOR)->addDays($totalDays);
    }

    public static function bsStringToAd(string $bs): Carbon
    {
        [$y, $m, $d] = array_map('intval', explode('-', $bs));
        return self::bsToAd($y, $m, $d);
    }

    // ════════════════════════════════════════════════════════════════
    //  DISPLAY HELPERS — use these in Blade views
    // ════════════════════════════════════════════════════════════════

    /**
     * Format AD date as BS string for display.
     * Formats: 'Y-m-d' | 'd M Y' | 'd M Y BS' | 'd/m/Y' | 'short' | 'Y' | 'm'
     */
    public static function format($date, string $fmt = 'Y-m-d'): string
    {
        if (!$date || $date === '0000-00-00' || $date === '0000-00-00 00:00:00') {
            return '—';
        }

        $bs = self::adToBs($date);
        if ($bs['year'] === 0) return '—';

        $y  = $bs['year'];
        $m  = $bs['month'];
        $d  = $bs['day'];
        $mn = $bs['month_name'];

        return match ($fmt) {
            'Y-m-d'    => sprintf('%d-%02d-%02d', $y, $m, $d),
            'd M Y'    => "{$d} {$mn} {$y}",
            'd M Y BS' => "{$d} {$mn} {$y} B.S.",
            'd/m/Y'    => sprintf('%d/%02d/%d', $d, $m, $y),
            'short'    => $d . ' ' . substr($mn, 0, 3) . ' ' . $y,
            'Y'        => (string) $y,
            'm'        => sprintf('%02d', $m),
            default    => sprintf('%d-%02d-%02d', $y, $m, $d),
        };
    }

    /** Today's date in BS */
    public static function today(): array
    {
        return self::adToBs(now());
    }

    /** Current BS year (e.g. 2082) */
    public static function currentYear(): int
    {
        return self::today()['year'];
    }

    /** Days in a given BS month */
    public static function daysInMonth(int $y, int $m): int
    {
        return self::$data[$y - self::BS_ANCHOR][$m] ?? 30;
    }

    /** All month names */
    public static function monthNames(): array
    {
        return self::$months;
    }

    /** Start of a BS year as AD Carbon (for DB queries) */
    public static function startOfBsYear(int $y): Carbon
    {
        return self::bsToAd($y, 1, 1);
    }

    /** End of a BS year as AD Carbon (for DB queries) */
    public static function endOfBsYear(int $y): Carbon
    {
        $idx = $y - self::BS_ANCHOR;
        return self::bsToAd($y, 12, self::$data[$idx][12]);
    }
